    <h1>¿Qué es JobJob y cómo funciona?</h1>
    <p>JobJob es una página web con la finalidad de que las personas puedan encontrar su empresa ideal para trabajar y dónde las empresas buscaran a sus trabajadores ideales.</p>

    <h1>¿Cómo funciona JobJob?</h1>
    <p>Mediante la función "swiper" podrás ir deslizando e ir viendo la gran variedad de personas o empresas que hay disponibles para tí. Desliza para la derecha si te interesa, si no, desliza a la izquierda y sigue buscando.</p>

    <h1>¿Cómo funcionan los match?</h1>
    <p>Para que se lleve a cabo un match, una persona y una empresa deben swipearse de forma mutua para crear un match.</p>

    <h1>¿Qué beneficios tiene usar JobJob?</h1>

    <h1>¿Es gratuito?</h1>
    <p>Sí, es completamente gratuito.</p>

    <h1>¿Puedo usar JobJob desde el ordenador?</h1>
    <p>Sí, la página web está diseñada para quie se pueda usar cómodamente tanto en móvil, tablet como ordenador.</p>

    <h1>¿Cómo puedo contactar con mi match?</h1>
    <p>Podéis comunicaros a través del chat que tenemos implementado en JobJob.</p>

    <h1>¿Cómo se si tengo algún match?</h1>
    <p>Una vez surge el match, podrás ver en el apartado de notificaciones que tienes un match con esa persona o empresa.</p>